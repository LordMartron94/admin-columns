<script>
	import ColumnSetting from "../ColumnSetting.svelte";
	import Select from "svelte-select"
	import {onMount} from "svelte";

	export let config;
	export let value;

	let collection = [];
	let selectValue;

	onMount( () => {
		collection = config.options.options
	} )

	const changeValue = ( d ) => {
		value = selectValue;
	}

	const groupBy = ( item ) => item.group;
</script>

<ColumnSetting label={config.label}>

		<Select
				--list-max-height="400px"
				class="-acui"
				showChevron
				items={collection}
				value={value}
				clearable={false}
				{groupBy}
				on:change={ changeValue }
				bind:justValue={selectValue}>

		</Select>

</ColumnSetting>